pub mod sha256;

#[cfg(test)]
mod tests {
    use crate::sha256;

    #[test]
    fn it_works() {
        let result = 2 + 2;
        assert_eq!(result, 4);
    }

    #[test]
    fn sha256_alice() {
        let expect_result = [
            0x99, 0xe5, 0x3c, 0xbb, 0x0a, 0xeb, 0x27, 0x43, 0x44, 0xa2, 0x54, 0x73, 0x3d, 0xb9,
            0x96, 0xca, 0x2d, 0x05, 0xd5, 0xfc, 0xd1, 0x0f, 0xc0, 0xca, 0x02, 0xd6, 0x96, 0x6f,
            0x2b, 0x2b, 0xc9, 0x61,
        ];

        let bytes = [0x01, 0x02, 0x03];

        let result = sha256::hash(&bytes);

        assert_eq!(result, expect_result);
    }
}
